package Encapsulation;

public class IBM 
{
String EmployeName;
String EmployeProfile;
double Salary;
int Id;
String Address;
public String getEmployeName() {
	return EmployeName;
}
public void setEmployeName(String employeName) {
	EmployeName = employeName;
}
public String getEmployeProfile() {
	return EmployeProfile;
}
public void setEmployeProfile(String employeProfile) {
	EmployeProfile = employeProfile;
}
public double getSalary() {
	return Salary;
}
public void setSalary(double salary) {
	Salary = salary;
}
public int getId() {
	return Id;
}
public void setId(int id) {
	Id = id;
}
public String getAddress() {
	return Address;
}
public void setAddress(String address) {
	Address = address;
}

}
